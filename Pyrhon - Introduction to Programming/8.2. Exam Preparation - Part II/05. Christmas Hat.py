n = int(input())
#draw top
print('.'*(n * 2 - 1) +'/|\\' + '.'*(n * 2 - 1))
print('.'*(n * 2 - 1) +'\\|/' + '.'*(n * 2 - 1))
print('.'*(n * 2 - 1) +'***' + '.'*(n * 2 - 1))
#draw middle
for i in range(n * 2 - 1):
    print('.'*(abs(n * 2 - 2 - i)) + '*' + '-'*(i + 1) + '*' + '-'*(i + 1) + '*' + '.'*(abs(n * 2 - 2 - i)))
  
#bottom
print('*'*(n * 4 + 1))
print('*.'*(n * 2 ) + '*')
print('*'*(n * 4 + 1))



